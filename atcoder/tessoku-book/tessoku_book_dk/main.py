N = int(input())
S = input()

# 左から順に高さを計算
# 'A' の場合は左の草より1高く、'B' の場合は高さを1にリセット
X = [1]  # 最初の草の高さは1
for s in S:
    if s == "A":
        X.append(X[-1] + 1)  # 左の草より高くする
    else:
        X.append(1)  # 高さを1にリセット

# 右から順に高さを計算
# 'B' の場合は右の草より1高く、'A' の場合は高さを1のまま
Y = [1] * N  # 初期値はすべて1
for i in range(N - 2, -1, -1):
    if S[i] == "B":
        Y[i] = Y[i + 1] + 1  # 右の草より高くする

# 左右の条件をマージして最終的な高さを決定
# 各草の高さは X[i] と Y[i] の最大値を取る
ans = sum(max(x, y) for x, y in zip(X, Y))

# 草の高さの合計を出力
print(ans)
