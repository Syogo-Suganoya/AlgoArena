# 入力を受け取る
n, k = map(int, input().split())
s = input()

# t に、文字と連続する個数を記録するための配列を用意
# 例えば "??oo?o" → [['?', 2], ['o', 2], ['?', 1], ['o', 1]]
t = [[s[0], 1]]

# もし最初の文字が 'o' なら、残りの 'o' 必要数から引く
k -= s[0] == "o"

# 2文字目以降を走査
for i in range(1, n):
    if s[i] != "?":
        # '?' でないならそのまま確定
        x = s[i]
    elif s[i - 1] == "o" or (i < n - 1 and s[i + 1] == "o"):
        # もし '?' だが、前後に 'o' があれば、制約で必ず '.' にする
        x = "."
    else:
        # まだどちらでも置ける
        x = "?"

    # 同じ文字が続くならカウントを増やす
    if t[-1][0] == x:
        t[-1][1] += 1
    else:
        t.append([x, 1])

    # もし決まったのが 'o' なら、残りの必要数から引く
    k -= x == "o"

# '?' 区間で、最大何個の 'o' が置けるか計算
# （？区間の長さvなら、最大 (v+1)//2 個まで 'o' が置ける）
y = 0
for c, v in t:
    if c != "?":
        continue
    y += (v + 1) // 2

# いよいよ出力する文字列を組み立てる
ans = ""
for c, v in t:
    if c != "?":
        # '?' でなければそのまま
        ans += c * v
    elif k == 0:
        # もう必要な 'o' はないなら全部 '.'
        ans += "." * v
    elif k == y and v & 1:
        # '?' の中で必ず決まるところ（残りの 'o' がちょうど y の場合）
        # 奇数長の ? 区間は交互に置いたあと、真ん中に 'o' が来る
        # 例: "?????" (5個) → "o.o.o"
        ans += "o." * (v // 2) + "o"
    else:
        # まだどちらか分からないなら '?'
        ans += "?" * v

print(ans)
