x, y, a, b = map(int, input().split())

ans = 0  # 操作回数のカウンタ

# -----------------------------
# 掛け算の方が「加算より効率が良く」、かつ「目標を超えない」限り掛け続ける
# 条件1: a * x <= x + b → 加算と比べて掛け算が効率的
# 条件2: a * x < y      → 掛け算後も y 未満である必要がある
# -----------------------------
while a * x <= x + b and a * x < y:
    x *= a  # 掛け算を実行
    ans += 1  # 操作回数をカウント

# -----------------------------
# 残りの距離は +b の加算で詰める
# x から y-1 までの距離を b で割って、何回加算すればよいかを求める
# ※ y 以上になる直前で止めたいから y-1 まで
# -----------------------------
ans += (y - 1 - x) // b

# 結果を出力
print(ans)
