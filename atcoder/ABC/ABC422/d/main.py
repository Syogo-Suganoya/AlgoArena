# 入力を受け取る
n, k = map(int, input().split())

# 配列の長さは 2^n
length = 1 << n  # 2 ** n と同じ意味

# k を 2^n で割った商をすべての要素の基本値にする
# これで「均等に分けられる部分」を初期化
a = [k >> n] * length  # k >> n は k // (2**n) と同じ

# k を 2^n で割った余りを更新
# 余りはまだ分配されていない「1 を加える分」
k %= length

# 余り k を均等に配列に分配
# i が 0 から k-1 までループ
for i in range(k):
    # i 番目の余りを配置する位置を計算
    # length-1 を k 分割
    a[(i << n) // k] += 1

# アンバランスさは最大値 - 最小値
# 余りがある場合は差が 1 になる
print(1 if k else 0)

# 配列を出力
print(*a)
