N = int(input())

# N から 2 の因子をすべて取り除く（後の約数探索の効率化）
# 等差数列の和の式から、2N = L*(2a + L - 1) を考えるが、
# 2の因子は a や L の中に吸収されて意味を持たないため、
# 先に除いておくことで探索対象が半分以下になる
while N % 2 == 0:
    N //= 2

# √N までの整数を試すために平方根を計算（整数部分だけ）
sq = int(N**0.5)

# 約数の個数をカウントする
# i が N の約数なら、N // i も約数になるので、片方を見れば2個得られる
# ただし、i * i == N のときは重複するので最後に調整
ans = sum(N % i == 0 for i in range(1, sq + 1)) * 2 - (sq * sq == N)

# 約数の個数を2倍することで、2N の約数の総数とし、それが答えになる
# （2 の因子を除いていたため、偶数の因数分をここで回復）
print(ans * 2)
