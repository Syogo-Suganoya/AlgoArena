s = input()
n = len(s)

a = []  # 文字列を1文字ずつ積むスタック
t = set()  # 現在のスコープ内で出現した文字を記録する集合

for i in range(n):
    a.append(s[i])  # 文字をスタックに積む

    if s[i] == "(":
        # 開きカッコはそのままスタックに積むだけ
        continue
    elif s[i] == ")":
        # 閉じカッコが来たら、直前の対応する開きカッコまでの間にあった文字を取り除く
        while a[-1] != "(":
            p = a.pop()  # スタックの上から1つずつ取り出す
            if p != ")":  # 文字の場合だけ集合から削除する
                t.remove(p)
        a.pop()  # "(" もスタックから取り除く
    else:
        # 英小文字が来た場合
        if s[i] in t:
            # すでにこのスコープ内に同じ文字があったらアウト
            print("No")
            exit()
        else:
            # 新しい文字なら、集合に記録する
            t.add(s[i])

# すべての文字を見終わったら、OK
print("Yes")
