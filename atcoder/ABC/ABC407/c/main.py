# 入力として文字列 S を受け取る
S = input().strip()

# 合計の操作回数を記録する変数
total = 0
N = len(S)  # S の桁数

# 末尾の桁から先頭に向かってループ
for i in range(N - 1, -1, -1):
    v = int(S[i])  # 現在の桁の数字
    # 次の桁の数字（存在する場合）、なければ 0 とする
    u = int(S[i + 1]) if i < N - 1 else 0
    # この桁で必要な操作回数を計算
    # (10 + v - u) % 10 で 0～9 の範囲の操作回数に調整
    b = (10 + v - u) % 10
    total += b  # 合計に加える

# 各桁での操作回数に加えて、すべての桁に1回ずつ押す操作が必要
ans = total + N
print(ans)
