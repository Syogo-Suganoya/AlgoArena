# 入力の文字列（数字）を取得
x = input().strip()

# s: まだ処理していない桁の合計
s = sum(int(d) for d in x)  # 各桁を整数に変換して合計
c = 0  # 繰り上がり用の変数
res = []  # 結果の各桁を格納（逆順で追加）

i = len(x) - 1  # 右端（最後の桁）から処理
while True:
    c += s  # 現在の桁と未処理桁の合計を加算
    res.append(str(c % 10))  # 下1桁を結果に追加
    c //= 10  # 繰り上がりを更新（10で割る）

    if i >= 0:
        s -= int(x[i])  # 右端の桁を処理済みにする

    # 終了条件: 全ての桁を処理済みで、繰り上がりもゼロ
    if i <= 0 and c == 0:
        break

    i -= 1  # 左に1桁ずらす

# 逆順で追加していたので、正しい順に並び替える
res.reverse()

# 結果を文字列として出力
print("".join(res))
