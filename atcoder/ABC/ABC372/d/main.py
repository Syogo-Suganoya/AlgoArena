n = int(input())
h = list(map(int, input().split()))
ans = [0] * n  # 各ビルから見えるビルの数を格納するリスト
stc = []  # スタック：現在のビルから右側に見えるビルのインデックスを保持

# 右端から1つ手前のビルまで逆順に処理していく
for i in range(n - 2, -1, -1):
    # スタックの一番上のビルが、現在のビルよりも低い場合は見えないので取り除く
    while stc and h[stc[-1]] < h[i + 1]:
        stc.pop()
    # 現在のビルの右隣のビルのインデックスをスタックに追加
    stc.append(i + 1)
    # スタックに入っているビルは、現在のビルから見えるビルなので、その数を記録
    ans[i] = len(stc)

# 結果を出力
print(*ans)
