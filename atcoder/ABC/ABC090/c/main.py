N, M = map(int, input().split())

# ケース①：1x1 のマスだけ。これは特別に1個とする。
if N == 1 and M == 1:
    print(1)

# ケース②：1行または1列だけ（細長いマス）
elif N == 1 or M == 1:
    # 端の2マスを除いた数（ただし全部で2マス以下のときは負にならないよう注意）
    print(max(0, N * M - 2))

# ケース③：それ以外（普通の大きさのマス）
else:
    # 上下左右の1列ずつを除いた内側のマスの数
    print((N - 2) * (M - 2))
