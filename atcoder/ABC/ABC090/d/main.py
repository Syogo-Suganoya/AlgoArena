N, K = map(int, input().split())

ans = 0

# bの値をK+1からNまで動かしながら考える
# b <= K のときは a % b が K 以上にならないので除外
for b in range(K + 1, N + 1):
    # N+1 までは (a % b) のパターンが何回繰り返されるか
    q = (N + 1) // b  # これは「完全にbで割れるグループの数」

    # 最後に少し余る部分（繰り返しきれなかった端数）
    r = (N + 1) % b

    # 各グループの中で、余りがK以上の数は (b - K) 個ある
    # それがq個あるので、q * (b - K) を加算
    ans += q * (b - K)

    # 最後に残った端数の部分の中に、K以上の余りがいくつあるか調べて加算
    # 端数は r 個あるので、K 以上のものは max(0, r - K) 個
    ans += max(0, r - K)

# もし K == 0 のとき、すべてのペアがカウントされるが、
# a % b == 0 のペアは含めちゃいけないので、その分を N 引く
if K == 0:
    ans -= N

print(ans)
